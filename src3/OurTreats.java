abstract public class OurTreats {
    
    /**method to enable 4 treats have their own version of the same name method.
     * @return String numeric value */
    abstract public String printMe();
    
    /**method to enable 2 made(s) have their own version of the same name method.
     * @return String numeric value  */
    abstract public String print2Made();
    
    /**method to enable 8 different chocolate objects to use their own interface method while declared at OurTreats level without casting down .
     * @return String numeric value */
    abstract public String printNut();
}
